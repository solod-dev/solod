// So has various value types including strings,
// integers, floats, booleans, etc. Here are a few
// basic examples.
package main

func main() {
	// Strings.
	println("so is awesome")

	// Integers and floats.
	println("1+1 =", 1+1)
	println("7.0/3.0 =", 7.0/3.0)

	// Booleans, with boolean operators as you'd expect.
	println(true && false)
	println(true || false)
	println(!true)
}
